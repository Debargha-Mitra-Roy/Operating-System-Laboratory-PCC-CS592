today=`date +%m-%d-%Y`
echo "Today: $today"

numberOfdays=`date +%j`
echo "The Current Day Number: $numberOfdays"

numberOfseconds=`date +%s`
echo "Total seconds passed this year: $numberOfseconds"

weekNumber=`date +%U`
echo "The week number of the year is: $weekNumber"

Time=`date +%T`
echo "The time is: $Time"
